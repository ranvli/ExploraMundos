    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int xL = 0;
        int yL = 0;

        try{

            xL = Integer.parseInt(jTextField3.getText());
            yL = Integer.parseInt(jTextField4.getText());

            if( xL < 5 || xL >14 || yL < 5 || yL >9){
                JOptionPane.showMessageDialog(rootPane, "Las dimensiones deben estar entre el rango 5x5 y 14x9.");
                return;
            }

            editor.xLargo = xL;
            editor.yLargo = yL;

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Las dimensiones deben ser n√∫meros enteros.");
            return;
        }



        editor.borraElementos();
        editor.nuevoMapa(xL, yL);
        editor.cargaElementos();

        this.setVisible(false);


    }//GEN-LAST:event_jButton2ActionPerformed

